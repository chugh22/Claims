package pojo;

public class QuickActionsStatus {
	Boolean download;
	Boolean inquiry;
	Boolean document;
	Boolean appeal;
	Boolean eop;
	Boolean eob ;
	public Boolean getEob() {
		return eob;
	}
	public void setEob(Boolean eob) {
		this.eob = eob;
	}
	public Boolean getDownload() {
		return download;
	}
	public void setDownload(Boolean download) {
		this.download = download;
	}
	public Boolean getInquiry() {
		return inquiry;
	}
	public void setInquiry(Boolean inquiry) {
		this.inquiry = inquiry;
	}
	public Boolean getDocument() {
		return document;
	}
	public void setDocument(Boolean document) {
		this.document = document;
	}
	public Boolean getAppeal() {
		return appeal;
	}
	public void setAppeal(Boolean appeal) {
		this.appeal = appeal;
	}
	public Boolean getEop() {
		return eop;
	}
	public void setEop(Boolean eop) {
		this.eop = eop;
	}
}
